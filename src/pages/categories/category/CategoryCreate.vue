<template>
  <VContainer fluid>
    <div class="d-flex justify-space-between align-center mb-4">
      <h1>Добавить Новую Категорию</h1>
    </div>

    <VRow>
      <VCol cols="12" md="8">
        <VCard variant="flat" class="rounded-lg">
          <VCardText>
            <VRow>
              <VCol cols="12">
                <VTextField
                  v-model="form.name"
                  label="Название Категории"
                  autofocus
                  variant="outlined"
                  density="compact"
                  hide-details="auto"
                  @keyup.enter="save"
                />
              </VCol>

              <VCol cols="12">
                <CategoriesAutocomplete
                  v-model="form.parentId"
                  label="Родительская Категория"
                />
              </VCol>
            </VRow>
          </VCardText>
        </VCard>

        <VCard variant="flat" class="rounded-lg">
          <VCardActions>
            <VSpacer/>

            <VBtn variant="text" :to="{ name: 'categories' }">
              Назад
            </VBtn>

            <VBtn color="primary" variant="flat" :loading="loading" @click="save">
              Сохранить
            </VBtn>
          </VCardActions>
        </VCard>
      </VCol>
    </VRow>
  </VContainer>
</template>

<script>
import api from '@/plugins/api.js'
import CategoriesAutocomplete from "@/components/CategoriesAutocomplete.vue"

export default {
  name: 'CategoryCreate',
  components: { CategoriesAutocomplete },

  data: () => ({
    loading: false,

    form: {
      name: undefined,
      parentId: undefined,
    }
  }),

  methods: {
    async save() {
      this.loading = true

      try {
        await api.categories.create(this.form)

        this.$router.push({ name: 'categories' })
      } finally {
        this.loading = false
      }
    }
  }
}
</script>
